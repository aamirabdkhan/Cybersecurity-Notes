# Rules of Naming a Variable
# 1. Can only contain letters, numbers and underscores
# 	 They can start with a letter or underscores, they can't start with a number
# 2. Spaces are not allowed in variable names but you can use undescores to separate words
# 3. Avoid using Python Keywords and functions names
# 4. Variable names should be short and descriptive
# 5. Be careful using the lowercase l and uppercase O because they can be confused with numbers 1 and 0.
